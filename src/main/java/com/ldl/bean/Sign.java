package com.ldl.bean;

public class Sign {
    String sid;
    String openid;
    int continuous_sign ;
    int all_sgin;

    public Sign() {
    }

    public String getSid() {
        return sid;
    }

    public void setSid(String sid) {
        this.sid = sid;
    }

    public String getOpenid() {
        return openid;
    }

    public void setOpenid(String openid) {
        this.openid = openid;
    }

    public int getContinuous_sign() {
        return continuous_sign;
    }

    public void setContinuous_sign(int continuous_sign) {
        this.continuous_sign = continuous_sign;
    }

    public int getAll_sgin() {
        return all_sgin;
    }

    public void setAll_sgin(int all_sgin) {
        this.all_sgin = all_sgin;
    }
}
